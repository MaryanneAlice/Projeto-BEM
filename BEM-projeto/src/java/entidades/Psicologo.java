package entidades;

/**
 *
 * @author marya
 */
public class Psicologo {
    
    private String nome, forma_atendimento, rua, numero,
            bairro, cidade, EMail, login, senha, crp, contato, contatoOp;

    public Psicologo() {
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getForma_atendimento() {
        return forma_atendimento;
    }

    public void setForma_atendimento(String forma_atendimento) {
        this.forma_atendimento = forma_atendimento;
    }

    public String getRua() {
        return rua;
    }

    public void setRua(String rua) {
        this.rua = rua;
    }

    public String getNumero() {
        return numero;
    }

    public void setNumero(String numero) {
        this.numero = numero;
    }

    public String getBairro() {
        return bairro;
    }

    public void setBairro(String bairro) {
        this.bairro = bairro;
    }

    public String getCidade() {
        return cidade;
    }

    public void setCidade(String cidade) {
        this.cidade = cidade;
    }

    public String getEMail() {
        return EMail;
    }

    public void setEMail(String EMail) {
        this.EMail = EMail;
    }

    public String getLogin() {
        return login;
    }

    public void setLogin(String login) {
        this.login = login;
    }

    public String getSenha() {
        return senha;
    }

    public void setSenha(String senha) {
        this.senha = senha;
    }

    public String getCrp() {
        return crp;
    }

    public void setCrp(String crp) {
        this.crp = crp;
    }

    public String getContato() {
        return contato;
    }

    public void setContato(String contato) {
        this.contato = contato;
    }

    public String getContatoOp() {
        return contatoOp;
    }

    public void setContatoOp(String contatoOp) {
        this.contatoOp = contatoOp;
    }
    
}